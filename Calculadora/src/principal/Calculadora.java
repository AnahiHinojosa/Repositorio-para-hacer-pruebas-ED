package principal;

public class Calculadora {
	public int Suma( int num1, int num2){
		return num1 + num2;
	}
	public int Rest (int num1,int num2) {
			return num1-num2;
	}
	public int Mult (int num1, int num2) {
		return num1 * num2;
	}
	public int Div (int num1, int num2) {
		return num1/num2;
	}
	
}
